/***************************************************
程序功能：点亮LED(D1)
---------------------------------------------------
测试说明：观察LED闪烁
***************************************************/
#include  <msp430x14x.h>

/****************主函数****************/
void main(void)
{  
    WDTCTL = WDTPW + WDTHOLD;       //关闭看门狗
 
    P2DIR = 0x01;                   //设置P2-BIT0口方向为输出
    P2OUT = 0xfe;                   //点亮D1
}


